            опытного образца, видео. В данном случае мы несколько упрощаем понятие MVP, т.к. в отдельных случаях не всегда удается выполнить
            полноценный действующий MVP. Однако хорошая презентация может достаточно убедительно продемонстрировать работоспособность ключевых
            разработанных функций перспективного продукта.
