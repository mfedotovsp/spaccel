            С самого начала проектанту необходимо сгенерировать формулировки сегментов, те, где он видит применение своего
            предполагаемого продукта. Однако на следующих шагах, при тестировании, может оказаться, что изначально сформулированные
            сегменты определены неверно и их нужно переформулировать. Поэтому первоначально сформулированные и неподтверждённые рынком
            сегменты называем гипотезы сегментов.
        </p>

        <p class="bold">Формулировка сегментов</p>

        <p>
            <span class="bold">Сегментация</span>
            &#8212; это разделение целевой аудитории или всего рынка на части по какому-либо критерию или процесс выявления определенных
